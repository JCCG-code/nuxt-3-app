import { defineMongooseModel } from "#nuxt/mongoose";

export const Event = defineMongooseModel(
  "Event",
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    startDate: {
      type: Date,
      required: true,
    },
    endDate: {
      type: Date,
      required: true,
    },
  },
  { timestamps: true }
);
